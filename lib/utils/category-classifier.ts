// 智能分类函数 - 基于明确关键词匹配，避免误分类
export function generateSmartTag(text: string): string | null {
  console.log('=== 分类函数调试 ===')
  console.log('原始输入:', text)

  const cleanText = text.toLowerCase().trim()
  console.log('清理后的文本:', cleanText)

  if (!cleanText || cleanText.length < 2) {
    console.log('文本太短，返回null')
    return null
  }

  // 只使用明确的长关键词，避免单字误匹配
  const categoryKeywords: Record<string, string[]> = {
    '自然科学': [
      '数学', '代数', '几何', '微积分', '物理', '化学', '生物', '科学', '实验',
      '光学', '电学', '磁学', '力学', '热学', '量子', '相对论', '能量', '原子', '分子', '细胞', '基因',
      '地球', '宇宙', '星球', '太阳系', '天气', '气候', '自然', '环境', '生态',
      '效应', '现象', '原理', '定律', '定理', '公式', '元素', '反应', '进化', '遗传'
    ],
    '工程技术': [
      '计算机', '软件', '程序', '代码', '网络', '网站', '网页', '互联网', '手机', '电脑',
      '编程', '开发', '系统', '应用', '人工智能', '机器学习', '深度学习',
      '电子', '电路', '芯片', '硬件', '设备', '机械', '工程', '建筑', '设计', '产品',
      '工具', '平台', '框架', '数据库', '算法', '数据', '自动化', '智能化'
    ],
    '医学健康': [
      '医学', '药物', '治疗', '手术', '健康', '身体', '康复', '营养', '饮食',
      '医院', '医生', '护士', '体检', '检查', '化验', '疫苗', '病毒', '细菌', '感染',
      '心理', '睡眠', '锻炼', '运动', '养生', '保健', '中医', '西医', '处方'
    ],
    '人文社科': [
      '历史', '文学', '诗歌', '小说', '散文', '戏剧', '艺术', '音乐', '舞蹈', '电影',
      '哲学', '思想', '文化', '语言', '文字', '写作', '阅读', '故事', '传说',
      '神话', '宗教', '信仰', '传统', '文明', '考古', '文物', '博物馆', '创作', '表演'
    ],
    '社会科学': [
      '政治', '经济', '金融', '市场', '投资', '股票', '银行', '理财',
      '商业', '企业', '公司', '工作', '职场', '团队', '组织', '领导',
      '教育', '学校', '学习', '培训', '考试', '知识', '法律', '案件', '权利', '义务',
      '国际', '外交', '政策', '制度', '民主', '选举', '政府', '国家', '民族', '人口'
    ]
  }

  // 直接在原始文本中搜索关键词
  for (const [category, keywords] of Object.entries(categoryKeywords)) {
    for (const keyword of keywords) {
      if (cleanText.includes(keyword.toLowerCase())) {
        console.log(`匹配到关键词 "${keyword}"，分类为: ${category}`)
        return category
      }
    }
  }

  // 如果没有匹配，返回 null
  console.log('未匹配到任何分类关键词')
  return null
}
